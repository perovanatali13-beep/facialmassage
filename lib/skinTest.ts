export interface TestResult {
  code: string;
  parts: { key: string; title: string; score: number; letter: string }[];
  info: SkinTypeInfo;
}

// Подсчёт результата по ответам. answers: ключ "<sectionKey>-<qIndex>" -> балл.
export function computeResult(answers: Record<string, number>): TestResult {
  const parts = sections.map((s) => {
    let score = 0;
    s.questions.forEach((_, qi) => {
      score += answers[`${s.key}-${qi}`] ?? 0;
    });
    const letter = score >= s.threshold ? s.high : s.low;
    return { key: s.key, title: s.title, score, letter };
  });
  // Порядок букв типа: D/O · S/R · P/N · W/T
  const code = parts.map((p) => p.letter).join("");
  return { code, parts, info: skinTypes[code] };
}

export const totalQuestions = sections.reduce((n, s) => n + s.questions.length, 0);
